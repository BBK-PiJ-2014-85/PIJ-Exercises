package e10;

public interface Animal {
	
		
	/**
	* Every animal should have a call which prints on screen
	*/
	void call();
	
	/**
	* Every animal should have a method to reproduce after the rain
	*/	
	void reproduce();
	
	/** 
	 * Every animal has a means to make sound
	 */
	
	void makeSound();
	
}

